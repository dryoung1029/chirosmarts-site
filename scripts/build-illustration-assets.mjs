/**
 * Pre-generates every derived raster asset ChiroSmarts serves statically.
 *
 * WHY NOT astro:assets at runtime: the site is SSR on Cloudflare. The adapter's
 * 'compile' image service is build-time-only for PRErendered pages; for SSR
 * pages <Picture> srcsets resolve through /_image, which on Cloudflare is a
 * passthrough that returns the ORIGINAL png (no sharp at the edge). That would
 * ship 4–5 MB PNGs and wreck Lighthouse. So we optimize here, at build, with
 * sharp, and serve static AVIF/WebP/PNG via a plain <picture> (see
 * src/components/marketing/Illustration.astro).
 *
 * Outputs (all committed):
 *   public/illustrations/<name>-<w>.{avif,webp}  responsive variants
 *   public/illustrations/<name>.png              small PNG fallback
 *   src/lib/illustration-manifest.ts             intrinsic AR + width ladder
 *   public/og-default.png   1200x630  wordmark over illustration-13
 *   public/email/certificate.png / renewal.png  ~600px, email-safe PNG
 *
 * Run: `npm run assets:illustrations`
 */
import sharp from "sharp";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { mkdir, writeFile } from "node:fs/promises";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const ill = (name) => join(root, "src/assets/illustrations", name);
const pub = (...p) => join(root, "public", ...p);

const CANVAS = "#fafaf7";
const BRAND = "#0b6b63";
const INK = "#13272b";

// Placed illustrations → the width ladder each is ever displayed at (×~2 for
// retina). Keep ladders tight so we don't commit variants we never request.
const PLACED = [
  { name: "becoming-ca", file: "illustration-02-becoming-ca.png", widths: [160, 240, 320] },
  { name: "time-to-renew", file: "illustration-03-time-to-renew.png", widths: [160, 240, 320] },
  { name: "run-a-clinic", file: "illustration-04-run-a-clinic.png", widths: [160, 240, 320] },
  { name: "hands-on-training", file: "illustration-05-hands-on-training.png", widths: [240, 360, 480] },
  { name: "vitals", file: "illustration-06-vitals.png", widths: [240, 360, 480] },
  { name: "certificate-moment", file: "illustration-07-certificate-moment.png", widths: [220, 330, 440] },
  { name: "evening-study", file: "illustration-08-evening-study.png", widths: [280, 360, 520, 680] },
  { name: "empty-state", file: "illustration-10-empty-state.png", widths: [160, 240, 320, 400] },
  { name: "clinic-dashboard", file: "illustration-11-clinic-dashboard.png", widths: [380, 560, 760, 1120, 1520] },
  { name: "oregon-path", file: "illustration-12-oregon-path.png", widths: [360, 540, 720, 1080, 1440] },
  { name: "error-404", file: "illustration-14-error-404.png", widths: [280, 420, 560, 840] },
];

async function buildResponsive() {
  await mkdir(pub("illustrations"), { recursive: true });
  const manifest = {};
  for (const item of PLACED) {
    const src = sharp(ill(item.file));
    const meta = await src.metadata();
    const ar = meta.width / meta.height;
    // Cap the ladder to the intrinsic width — never upscale.
    const widths = [...new Set(item.widths.map((w) => Math.min(w, meta.width)))].sort(
      (a, b) => a - b,
    );
    for (const w of widths) {
      await sharp(ill(item.file)).resize(w).avif({ quality: 55 })
        .toFile(pub("illustrations", `${item.name}-${w}.avif`));
      await sharp(ill(item.file)).resize(w).webp({ quality: 72 })
        .toFile(pub("illustrations", `${item.name}-${w}.webp`));
    }
    // One mid-size PNG fallback (ultimate compatibility).
    const fbW = Math.min(720, widths[widths.length - 1]);
    await sharp(ill(item.file)).resize(fbW).png({ quality: 80, compressionLevel: 9 })
      .toFile(pub("illustrations", `${item.name}.png`));

    manifest[item.name] = { ar: Math.round(ar * 1000) / 1000, widths, fallbackWidth: fbW };
    console.log(`illustrations/${item.name}: ${widths.join(",")} (avif+webp), png@${fbW}`);
  }
  const ts =
    "// AUTO-GENERATED by scripts/build-illustration-assets.mjs — do not edit by hand.\n" +
    "// Run `npm run assets:illustrations` to regenerate.\n" +
    "export interface IllustrationMeta { ar: number; widths: number[]; fallbackWidth: number; }\n" +
    `export const ILLUSTRATIONS = ${JSON.stringify(manifest, null, 2)} as const satisfies Record<string, IllustrationMeta>;\n` +
    "export type IllustrationName = keyof typeof ILLUSTRATIONS;\n";
  await writeFile(join(root, "src/lib/illustration-manifest.ts"), ts);
  console.log("wrote src/lib/illustration-manifest.ts");
}

// --- OG card: 1200x630, illustration-13 with the wordmark in the left space ---
async function buildOg() {
  const W = 1200;
  const H = 630;
  const bg = await sharp(ill("illustration-13-og-background.png"))
    .resize(W, H, { fit: "cover", position: "right" })
    .toBuffer();
  const overlay = Buffer.from(`<svg width="${W}" height="${H}" xmlns="http://www.w3.org/2000/svg">
    <defs>
      <linearGradient id="wash" x1="0" y1="0" x2="1" y2="0">
        <stop offset="0" stop-color="${CANVAS}" stop-opacity="0.96"/>
        <stop offset="0.5" stop-color="${CANVAS}" stop-opacity="0.86"/>
        <stop offset="0.78" stop-color="${CANVAS}" stop-opacity="0"/>
      </linearGradient>
    </defs>
    <rect x="0" y="0" width="${W}" height="${H}" fill="url(#wash)"/>
    <g font-family="system-ui, -apple-system, Segoe UI, Helvetica, Arial, sans-serif">
      <text x="80" y="300" font-size="84" font-weight="800" fill="${BRAND}" letter-spacing="-1.5">ChiroSmarts</text>
      <text x="82" y="362" font-size="34" font-weight="600" fill="${INK}">Oregon Chiropractic Assistant training</text>
      <text x="82" y="406" font-size="34" font-weight="600" fill="${INK}">&amp; automatic renewal tracking</text>
    </g>
  </svg>`);
  await sharp(bg).composite([{ input: overlay, top: 0, left: 0 }]).png({ quality: 90 })
    .toFile(pub("og-default.png"));
  console.log("wrote public/og-default.png (1200x630)");
}

// --- Email images: ~600px wide PNG, flattened on canvas (clients lack WebP/AVIF) ---
async function buildEmail(srcName, outName) {
  await mkdir(pub("email"), { recursive: true });
  await sharp(ill(srcName)).resize(600, null, { fit: "inside" })
    .flatten({ background: CANVAS }).png({ quality: 88 })
    .toFile(pub("email", outName));
  console.log(`wrote public/email/${outName}`);
}

await buildResponsive();
await buildOg();
await buildEmail("illustration-07-certificate-moment.png", "certificate.png");
await buildEmail("illustration-09-renewal-reminder.png", "renewal.png");
